const targetBoxes = document.querySelectorAll('.desc-box');

document.getElementById('C').addEventListener('click', function(event){
    var targetElement = document.querySelector('.skills-grid');
    if (targetElement) {
        targetElement.style.justifyContent = "start";
        targetElement.style.marginLeft = "14vw";
    }
    targetBoxes.forEach((box) => {
        box.style.opacity = "0";
        box.style.zIndex = "-1";
    })
    var targetBox = document.querySelector('#description-box1');
    if (targetBox) {
        targetBox.style.opacity = "1";
        targetBox.style.zIndex = "1";
    }
});
document.getElementById('close1').addEventListener('click',function(event){
    var targetElement = document.querySelector('.skills-grid');
    if (targetElement) {
        targetElement.style.justifyContent = "center";
        targetElement.style.marginLeft = "0px";
    }

    var targetBox = document.querySelector('#description-box1');
    if (targetBox) {
        targetBox.style.opacity = "0";
        targetBox.style.zIndex = "-1";
    }
    targetBoxes.forEach((box) => {
        box.style.opacity = "0";
        box.style.zIndex = "-1";
    })
});


